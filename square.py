def area(a): #функция area(a) принимает один аргумент, возвращает принятый аргумент в квадарте  
    #area(a) -> int
    return a * a #area(4) -> 16


def perimeter(a): #функция perimeter(a) принимает один аргумент возвращает принятый аргумент, умноженный в 4 раза
    #perimeter(a) -> int
    return 4 * a #perimeter(2) -> 8
