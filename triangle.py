def area(a, h): #функция area принимает два аргумента, возвращает полупроизведение аргументов
    #area(a, h) -> int
    return a * h / 2 #area(2,4) -> 4

def perimeter(a, b, c): #функция perimeter принимает три аргумента, возвращает их сумму
    #perimeter(a, b, c) -> int
    return a + b + c #perimeter(3, 4, 5) -> 12
