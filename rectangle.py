def area(a, b): #функция area(a, b) принимает два аргумента, возвращает их произведение
        #area(a, b) -> int
        return a * b  # area(5, 6) -> 30

def perimeter(a, b): #функция perimeter(a, b) принимает два аргумента, возвращает их удвоенную сумму
        #perimeter(a, b) -> int
        return 2*(a + b) #perimeter(5, 6) -> 22
